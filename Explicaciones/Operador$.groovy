/*
El operador "$" en Groovy se utiliza para interpolar variables dentro de una cadena de texto. Es similar a la interpolación de variables en Ruby o la sintaxis de plantilla de cadena en Python.

Por ejemplo, si tienes una variable llamada "nombre" con el valor "Juan", y quieres incluirlo en una cadena de texto, puedes hacer lo siguiente:
*/
def nombre = "Juan"
println "Hola, mi nombre es $nombre"
/* El resultado de la impresión será:
Hola, mi nombre es Juan

El operador "$" indica que se debe 
reemplazar el valor de la variable 
dentro de la cadena de texto. 
También puedes incluir expresiones 
dentro de las llaves {} dentro del
 operador "$" para realizar 
 operaciones o evaluaciones. */

def precio = 10.5
def cantidad = 2
println "El precio total es: ${precio * cantidad}"
